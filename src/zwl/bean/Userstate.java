public class Userstate {
	private Integer usid;
	private String ustate;
	private String usreason;
	private String usremark;
	public Integer getUsid() {
		return usid;
	}
	public void setUsid(Integer usid) {
		this.usid = usid;
	}
	public String getUstate() {
		return ustate;
	}
	public void setUstate(String ustate) {
		this.ustate = ustate;
	}
	public String getUsreason() {
		return usreason;
	}
	public void setUsreason(String usreason) {
		this.usreason = usreason;
	}
	public String getUsremark() {
		return usremark;
	}
	public void setUsremark(String usremark) {
		this.usremark = usremark;
	}
}
